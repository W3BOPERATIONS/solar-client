import React, { useState, useEffect } from 'react';
import {
  FileText,
  Plus,
  Edit,
  Trash2,
  CheckCircle,
  XCircle,
  Search,
  Loader,
  User,
  Truck,
  Box
} from 'lucide-react';
import {
  getVendorDeliveryPlans,
  createVendorDeliveryPlan,
  updateVendorDeliveryPlan,
  deleteVendorDeliveryPlan,
  getDeliveryTypes,
  getVehicles
} from '../../../../services/delivery/deliveryApi';
import {
  getInstallerVendors,
  getSupplierVendors
} from '../../../../services/vendor/vendorApi';

const VendorDeliveryPlan = () => {
  // State
  const [plans, setPlans] = useState([]);
  const [vendors, setVendors] = useState([]);
  const [deliveryTypes, setDeliveryTypes] = useState([]);
  const [vehicles, setVehicles] = useState([]);

  const [loading, setLoading] = useState(true);
  const [showModal, setShowModal] = useState(false);
  const [editingPlan, setEditingPlan] = useState(null);

  const [formData, setFormData] = useState({
    vendor: '',
    vendorModel: 'InstallerVendor',
    deliveryType: '',
    vehicle: '',
    pricePerDelivery: '',
    status: 'active'
  });

  const [searchTerm, setSearchTerm] = useState('');
  const [notification, setNotification] = useState({ show: false, message: '', type: '' });

  // Fetch Data
  useEffect(() => {
    loadData();
  }, []);

  const loadData = async () => {
    try {
      setLoading(true);
      const [plansRes, installersRes, suppliersRes, typesRes, vehiclesRes] = await Promise.all([
        getVendorDeliveryPlans(),
        getInstallerVendors(),
        getSupplierVendors(),
        getDeliveryTypes(),
        getVehicles()
      ]);

      if (plansRes.success) setPlans(plansRes.data);

      // Combine vendors
      const installers = (installersRes.data || []).map(v => ({ ...v, type: 'Installer', model: 'InstallerVendor' }));
      const suppliers = (suppliersRes.data || []).map(v => ({ ...v, type: 'Supplier', model: 'SupplierVendor' }));

      const combinedVendors = [...installers, ...suppliers];
      setVendors(combinedVendors);

      if (typesRes.success) setDeliveryTypes(typesRes.data);
      if (vehiclesRes.success) setVehicles(vehiclesRes.data);

    } catch (error) {
      showNotification('Failed to load data', 'error');
      console.error(error);
    } finally {
      setLoading(false);
    }
  };

  const refreshPlans = async () => {
    try {
      const response = await getVendorDeliveryPlans();
      if (response.success) setPlans(response.data);
    } catch (error) {
      console.error(error);
    }
  };

  // Notification Helper
  const showNotification = (message, type) => {
    setNotification({ show: true, message, type });
    setTimeout(() => {
      setNotification({ show: false, message: '', type: '' });
    }, 3000);
  };

  // Form Handling
  const handleInputChange = (e) => {
    const { name, value } = e.target;

    // Special handling for vendor select to capture model type
    if (name === 'vendor') {
      const selectedVendor = vendors.find(v => v._id === value);
      setFormData({
        ...formData,
        vendor: value,
        vendorModel: selectedVendor ? selectedVendor.model : 'InstallerVendor'
      });
    } else {
      setFormData({ ...formData, [name]: value });
    }
  };

  const resetForm = () => {
    setFormData({
      vendor: '',
      vendorModel: 'InstallerVendor',
      deliveryType: '',
      vehicle: '',
      pricePerDelivery: '',
      status: 'active'
    });
    setEditingPlan(null);
  };

  const handleOpenModal = (plan = null) => {
    if (plan) {
      setEditingPlan(plan);
      setFormData({
        vendor: plan.vendor?._id || '',
        vendorModel: plan.vendorModel || 'InstallerVendor',
        deliveryType: plan.deliveryType?._id || '',
        vehicle: plan.vehicle?._id || '',
        pricePerDelivery: plan.pricePerDelivery,
        status: plan.status
      });
    } else {
      resetForm();
    }
    setShowModal(true);
  };

  const handleCloseModal = () => {
    setShowModal(false);
    resetForm();
  };

  // CRUD Operations
  const handleSubmit = async (e) => {
    e.preventDefault();
    if (!formData.vendor || !formData.deliveryType || !formData.vehicle || !formData.pricePerDelivery) {
      showNotification('All fields are required', 'error');
      return;
    }

    try {
      if (editingPlan) {
        await updateVendorDeliveryPlan(editingPlan._id, formData);
        showNotification('Plan updated successfully', 'success');
      } else {
        await createVendorDeliveryPlan(formData);
        showNotification('Plan created successfully', 'success');
      }
      refreshPlans();
      handleCloseModal();
    } catch (error) {
      showNotification(error.response?.data?.message || 'Operation failed', 'error');
    }
  };

  const handleDelete = async (id) => {
    if (window.confirm('Are you sure you want to delete this plan?')) {
      try {
        await deleteVendorDeliveryPlan(id);
        showNotification('Plan deleted successfully', 'success');
        refreshPlans();
      } catch (error) {
        showNotification('Delete failed', 'error');
      }
    }
  };

  const handleStatusToggle = async (plan) => {
    try {
      const newStatus = plan.status === 'active' ? 'inactive' : 'active';
      await updateVendorDeliveryPlan(plan._id, { status: newStatus });
      showNotification('Status updated', 'success');
      refreshPlans();
    } catch (error) {
      showNotification('Status update failed', 'error');
    }
  };

  // Filter
  const filteredPlans = plans.filter(plan =>
    plan.vendor?.name?.toLowerCase().includes(searchTerm.toLowerCase()) ||
    plan.vehicle?.name?.toLowerCase().includes(searchTerm.toLowerCase())
  );

  return (
    <div className="min-h-screen bg-gray-50 p-6">
      {/* Notification Toast */}
      {notification.show && (
        <div className={`fixed top-4 right-4 z-50 p-4 rounded-lg shadow-lg text-white ${notification.type === 'success' ? 'bg-green-500' : 'bg-red-500'
          }`}>
          {notification.message}
        </div>
      )}

      {/* Header */}
      <div className="mb-6 flex justify-between items-center">
        <div>
          <h1 className="text-2xl font-bold text-gray-800 flex items-center">
            <FileText className="w-8 h-8 text-blue-600 mr-3" />
            Vendor Delivery Plans
          </h1>
          <p className="text-gray-600">Manage delivery rates for vendors</p>
        </div>
        <button
          onClick={() => handleOpenModal()}
          className="bg-blue-600 hover:bg-blue-700 text-white px-4 py-2 rounded-lg flex items-center transition-colors"
        >
          <Plus className="w-5 h-5 mr-2" />
          Add New Plan
        </button>
      </div>

      {/* Search and List */}
      <div className="bg-white rounded-xl shadow-sm p-6">
        <div className="mb-6 relative">
          <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400 w-5 h-5" />
          <input
            type="text"
            placeholder="Search by vendor or vehicle..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="w-full pl-10 pr-4 py-2 border border-gray-200 rounded-lg focus:ring-2 focus:ring-blue-500"
          />
        </div>

        {loading ? (
          <div className="flex justify-center py-12">
            <Loader className="w-8 h-8 text-blue-600 animate-spin" />
          </div>
        ) : filteredPlans.length === 0 ? (
          <div className="text-center py-12 text-gray-500">
            No vendor delivery plans found.
          </div>
        ) : (
          <div className="overflow-x-auto">
            <table className="w-full">
              <thead className="bg-gray-50 text-gray-600 text-sm">
                <tr>
                  <th className="px-6 py-3 text-left">Vendor</th>
                  <th className="px-6 py-3 text-left">Delivery Type</th>
                  <th className="px-6 py-3 text-left">Vehicle</th>
                  <th className="px-6 py-3 text-left">Price/Delivery</th>
                  <th className="px-6 py-3 text-left">Status</th>
                  <th className="px-6 py-3 text-right">Actions</th>
                </tr>
              </thead>
              <tbody className="divide-y divide-gray-100">
                {filteredPlans.map((plan) => (
                  <tr key={plan._id} className="hover:bg-gray-50">
                    <td className="px-6 py-4">
                      <div className="flex items-center">
                        <div className="bg-blue-100 p-2 rounded-full mr-3">
                          <User className="w-4 h-4 text-blue-600" />
                        </div>
                        <div>
                          <p className="font-semibold text-gray-800">{plan.vendor?.name || 'Unknown'}</p>
                          <p className="text-xs text-gray-500">{plan.vendor?.type || 'Vendor'}</p>
                        </div>
                      </div>
                    </td>
                    <td className="px-6 py-4">
                      <div className="flex items-center">
                        <Box className="w-4 h-4 text-gray-400 mr-2" />
                        <span className="text-gray-700">{plan.deliveryType?.name || 'Unknown'}</span>
                      </div>
                    </td>
                    <td className="px-6 py-4">
                      <div className="flex items-center">
                        <Truck className="w-4 h-4 text-gray-400 mr-2" />
                        <span className="text-gray-700">{plan.vehicle?.name || 'Unknown'}</span>
                      </div>
                    </td>
                    <td className="px-6 py-4 font-bold text-gray-800">
                      ₹{plan.pricePerDelivery}
                    </td>
                    <td className="px-6 py-4">
                      <button
                        onClick={() => handleStatusToggle(plan)}
                        className={`inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium cursor-pointer ${plan.status === 'active' ? 'bg-green-100 text-green-800' : 'bg-red-100 text-red-800'
                          }`}
                      >
                        {plan.status === 'active' ? 'Active' : 'Inactive'}
                      </button>
                    </td>
                    <td className="px-6 py-4 text-right">
                      <div className="flex justify-end space-x-2">
                        <button
                          onClick={() => handleOpenModal(plan)}
                          className="p-1 text-blue-600 hover:bg-blue-50 rounded"
                          title="Edit"
                        >
                          <Edit className="w-4 h-4" />
                        </button>
                        <button
                          onClick={() => handleDelete(plan._id)}
                          className="p-1 text-red-600 hover:bg-red-50 rounded"
                          title="Delete"
                        >
                          <Trash2 className="w-4 h-4" />
                        </button>
                      </div>
                    </td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        )}
      </div>

      {/* Modal */}
      {showModal && (
        <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50 p-4">
          <div className="bg-white rounded-xl shadow-xl max-w-md w-full p-6">
            <h2 className="text-xl font-bold text-gray-800 mb-4">
              {editingPlan ? 'Edit Plan' : 'Add New Plan'}
            </h2>
            <form onSubmit={handleSubmit}>
              <div className="space-y-4">
                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Vendor <span className="text-red-500">*</span>
                  </label>
                  <select
                    name="vendor"
                    value={formData.vendor}
                    onChange={handleInputChange}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500"
                    required
                  >
                    <option value="">Select Vendor</option>
                    {vendors.map(v => (
                      <option key={v._id} value={v._id}>{v.name} ({v.type})</option>
                    ))}
                  </select>
                </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Delivery Type <span className="text-red-500">*</span>
                  </label>
                  <select
                    name="deliveryType"
                    value={formData.deliveryType}
                    onChange={handleInputChange}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500"
                    required
                  >
                    <option value="">Select Delivery Type</option>
                    {deliveryTypes.filter(t => t.status === 'active').map(t => (
                      <option key={t._id} value={t._id}>{t.name}</option>
                    ))}
                  </select>
                </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Vehicle <span className="text-red-500">*</span>
                  </label>
                  <select
                    name="vehicle"
                    value={formData.vehicle}
                    onChange={handleInputChange}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500"
                    required
                  >
                    <option value="">Select Vehicle</option>
                    {vehicles.filter(v => v.status === 'active').map(v => (
                      <option key={v._id} value={v._id}>{v.name}</option>
                    ))}
                  </select>
                </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Price Per Delivery (₹) <span className="text-red-500">*</span>
                  </label>
                  <input
                    type="number"
                    name="pricePerDelivery"
                    value={formData.pricePerDelivery}
                    onChange={handleInputChange}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500"
                    placeholder="0.00"
                    min="0"
                    required
                  />
                </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Status
                  </label>
                  <select
                    name="status"
                    value={formData.status}
                    onChange={handleInputChange}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500"
                  >
                    <option value="active">Active</option>
                    <option value="inactive">Inactive</option>
                  </select>
                </div>
              </div>

              <div className="flex justify-end space-x-3 mt-6">
                <button
                  type="button"
                  onClick={handleCloseModal}
                  className="px-4 py-2 text-gray-700 bg-gray-100 hover:bg-gray-200 rounded-lg transition-colors"
                >
                  Cancel
                </button>
                <button
                  type="submit"
                  className="px-4 py-2 text-white bg-blue-600 hover:bg-blue-700 rounded-lg transition-colors"
                >
                  {editingPlan ? 'Update' : 'Create'}
                </button>
              </div>
            </form>
          </div>
        </div>
      )}
    </div>
  );
};

export default VendorDeliveryPlan;